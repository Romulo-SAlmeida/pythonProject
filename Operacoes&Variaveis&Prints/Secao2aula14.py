#Utilizado aspas simples e aspas duplas para printar informação na tela do ususario
print('Essa string é um "texto"')
print("Essa string é um 'texto'")

#Utilizando barra invertida para printar imformações com simbolos iguais
print("Essa string é um \"texto\"")
print('Essa', 'string', 'é', 'um', '\'texto\'', sep = '-')

#Utilizando o 'r' para cancelar qualquer operação dentro do print
print(r"Essa string é um \n'texto'")